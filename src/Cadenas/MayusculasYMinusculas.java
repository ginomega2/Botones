package Cadenas;

// *****************************   CNVERTIR DE MINUSCULAS A MAYUSCULAS E INVERSA
public class MayusculasYMinusculas {
    public static void main(String[] args) {
        String nombreMinusculas="krsna";
        String nombreMayusculas= "KRSNA";

        System.out.println("palabra en minusculas original "+nombreMinusculas);
        System.out.println("palabra en mayusculas usando toUpperCase()  "+nombreMinusculas.toUpperCase());

        System.out.println("palabra en mayusculas original "+nombreMinusculas);
        System.out.println("palabra en minusculas  usando toLowerCase()  "+nombreMinusculas.toLowerCase());

    }
}
